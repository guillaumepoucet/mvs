                    <h4>Que sont les cookies ?</h4>
                    <div class="espace"></div>

                    <p> Les cookies sont de petits fichiers texte qui viennent se placer automatiquement sur votre
                        ordinateur ou appareil mobile presque chaque fois que vous visitez un site Internet. Ils sont
                        stockés par votre navigateur. Les cookies contiennent des informations de base concernant
                        l’utilisation que vous faites d’Internet, mais ils ne vous identifient pas personnellement.
                        Votre
                        navigateur renvoie ces cookies au site lors de toute nouvelle visite de celui-ci, si bien qu’il
                        peut
                        reconnaître votre ordinateur ou appareil mobile et personnaliser et améliorer votre navigation
                        sur
                        le site. </p>

                    <p>Par exemple, nous utilisons des cookies sur notre site Internet afin de mémoriser vos données de
                        connexion, vous évitant ainsi d’avoir à les saisir à nouveau à plusieurs reprises lors de votre
                        visite du site. D’autres cookies nous permettent de comprendre ce qui vous a intéressé et ce qui
                        ne
                        vous a pas intéressé sur notre site ; cela nous permet de vous fournir des fonctionnalités qui
                        vous
                        conviennent mieux et qui vous seront plus utiles lors de votre prochaine visite. Nous utilisons
                        également des cookies pour mesurer l’efficacité de la publicité présente sur notre site et la
                        manière dont les visiteurs utilisent le site.</p>

                    <p>Certains cookies, appelés « cookies de session », ne résident dans votre ordinateur que lorsque
                        votre navigateur est ouvert et ils sont effacés automatiquement dès lors que vous fermez votre
                        navigateur. D’autres cookies, appelés « cookies permanents », continuent de résider dans votre
                        ordinateur ou appareil mobile après que vous avez fermé votre navigateur. De cette façon, les
                        sites
                        reconnaissent votre ordinateur ou appareil mobile lorsque vous rouvrez votre navigateur par la
                        suite, ce qui permet d’assurer une navigation aussi aisée que possible.</p>

